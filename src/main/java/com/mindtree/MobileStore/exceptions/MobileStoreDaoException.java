package com.mindtree.MobileStore.exceptions;

public class MobileStoreDaoException extends AppException {

	public MobileStoreDaoException() {
		super();
		
	}

	public MobileStoreDaoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public MobileStoreDaoException(String arg0) {
		super(arg0);
		
	}

	public MobileStoreDaoException(Throwable arg0) {
		super(arg0);
		
	}

}
