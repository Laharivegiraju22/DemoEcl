package com.mindtree.MobileStore.exceptions;

public class MobileStoreDbexception extends AppException {

	public MobileStoreDbexception() {
		super();
		
	}

	public MobileStoreDbexception(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MobileStoreDbexception(String arg0) {
		super(arg0);
		
	}

	public MobileStoreDbexception(Throwable arg0) {
		super(arg0);
		
	}

}
